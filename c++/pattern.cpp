int main(){

    int n;
    cout<<"enter the number :";
    cin>>n;//n=6;
    

    int i=1;
    while(i<=n+1){
        int j=1;
        while(j<=n+2-i){
            cout<<j<<" ";
            j++;
        }
        j=0;
        while(j<i-1){
            cout<<"*"<<" ";
            j++;

        }
        j=0;
        int start = i-1;
        while(start){
            cout<<"*"<<" ";
            start--;
        }
        i++;
        cout<<endl;
    }
}




